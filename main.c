#include "XMC1100.h"
#include "cy_utils.h"
#include "cybsp.h"
#include "cycfg_peripherals.h"
#include "xmc_ccu4.h"
#include <stdint.h>

// Betriebsmodi
#define MODE_IDLE 0x0
#define MODE_RL   0x1
#define MODE_LR   0x2
#define MODE_BP   0x3

// Output pin setter Traco 1
#define PIN_PRI_1_PS  PORT0_OMR_PS0_Msk
#define PIN_SEK_1_PS  PORT0_OMR_PS5_Msk
#define PIN_CRTL_1_PS PORT0_OMR_PS6_Msk
// Output pin setter Traco 2
#define PIN_PRI_2_PS  PORT0_OMR_PS7_Msk
#define PIN_SEK_2_PS  PORT0_OMR_PS8_Msk
#define PIN_CRTL_3_PS PORT0_OMR_PS9_Msk
// Output pin resetter Traco 1
#define PIN_PRI_1_PR  PORT0_OMR_PR0_Msk
#define PIN_SEK_1_PR  PORT0_OMR_PR5_Msk
#define PIN_CRTL_1_PR PORT0_OMR_PR6_Msk
// Output pin resetter Traco 2
#define PIN_PRI_2_PR  PORT0_OMR_PR7_Msk
#define PIN_SEK_2_PR  PORT0_OMR_PR8_Msk
#define PIN_CRTL_3_PR PORT0_OMR_PR9_Msk

// Output states MODE_RL
#define MODE_RL_OFF   (PIN_PRI_1_PS | PIN_SEK_1_PR | PIN_CRTL_1_PS | PIN_PRI_2_PR | PIN_SEK_2_PR | PIN_CRLT_2_PR)
#define MODE_RL_ON    (PIN_PRI_1_PS | PIN_SEK_1_PS | PIN_CRTL_1_PS | PIN_PRI_2_PR | PIN_SEK_2_PR | PIN_CRLT_2_PR)
// Output state MODE_LR
#define MODE_LR_OFF   (PIN_PRI_1_PR | PIN_SEK_1_PR | PIN_CRTL_1_PR | PIN_PRI_2_PS | PIN_SEK_2_PR | PIN_CRLT_2_PS)
#define MODE_LR_ON    (PIN_PRI_1_PR | PIN_SEK_1_PR | PIN_CRTL_1_PR | PIN_PRI_2_PS | PIN_SEK_2_PS | PIN_CRLT_2_PS)
// Output state MODE_BP
#define MODE_BP_OFF   (PIN_PRI_1_PS | PIN_SEK_1_PR | PIN_CRTL_1_PS | PIN_PRI_2_PS | PIN_SEK_2_PR | PIN_CRLT_2_PS)
#define MODE_BP_RL_ON (PIN_PRI_1_PS | PIN_SEK_1_PS | PIN_CRTL_1_PS | PIN_PRI_2_PS | PIN_SEK_2_PR | PIN_CRLT_2_PS)
#define MODE_BP_LR_ON (PIN_PRI_1_PS | PIN_SEK_1_PR | PIN_CRTL_1_PS | PIN_PRI_2_PS | PIN_SEK_2_PS | PIN_CRLT_2_PS)

void ccu4_0_SR0_INTERRUPT_HANDLER() {
  XMC_CCU4_SLICE_ClearEvent(timerMSB_HW, XMC_CCU4_SLICE_IRQ_ID_PERIOD_MATCH);
  PORT0->OMR |= (PORT0_OMR_PS0_Msk | PORT0_OMR_PR0_Msk);
}

int main(void) {
  cy_rslt_t result;

  /* Initialize the device and board peripherals */
  result = cybsp_init();
  if (result != CY_RSLT_SUCCESS) {
    CY_ASSERT(0);
  }

  uint32_t periodValue = 16000U - 1;
  uint16_t msbValue = (uint16_t)(periodValue >> 16);
  uint16_t lsbValue = (uint16_t)(periodValue / (msbValue + 1));

  NVIC_SetPriority(ccu4_0_SR0_IRQN, 0U);
  NVIC_EnableIRQ(ccu4_0_SR0_IRQN);
  XMC_CCU4_SLICE_SetTimerPeriodMatch(timerLSB_HW, lsbValue);
  XMC_CCU4_SLICE_SetTimerPeriodMatch(timerMSB_HW, msbValue);
  XMC_CCU4_EnableShadowTransfer(ccu4_0_HW, (XMC_CCU4_SHADOW_TRANSFER_SLICE_0 | XMC_CCU4_SHADOW_TRANSFER_SLICE_1));
  for (;;) {
  }
}

